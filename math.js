// double - this simple function doubles a number
function double(x) {
  return x * 2;
};

// square - this simple function squares a number, or multiplies it by itself
function square(x) {
  return x * x;
};

// isEven - this simple function checks if a number is even and confirms or refutes
function isEven(x) {
  if (x % 2 === -0)
    console.log("This number is even");
  else
    console.log("This number is either odd or not a number.");
}